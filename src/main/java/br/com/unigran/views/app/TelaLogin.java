    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        loginController = new LoginController();
        
        String username = campoLogin.getText();
        String senha = campoSenha.getText();
        
        LoginDTO buscaLogin = loginController.autenticaUsuario(username, senha);
//        System.out.println(buscaLogin);
        if(buscaLogin != null){
            new TelaAgendamento().setVisible(true);
            dispose();
        }else{
            resetaCampos();
            JOptionPane.showMessageDialog(this, "Usuario ou senha invalidos", "Login Invalido", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void resetaCampos(){
        campoLogin.setText(null);
        campoSenha.setText(null);
    }
    
